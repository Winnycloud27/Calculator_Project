int add(int a, int b)
{
    int sum = 0;
    sum = a + b;
    return sum;
}

int minus(int a, int b)
{
    int subtract = 0;
    subtract = a - b;
    return subtract;
}

int times(int a, int b)
{
    int multiple = 0;
    multiple = a * b;
    return multiple;
}

int divide(int a, int b)
{
    int divi = 0;
    divi = a / b;
    return divi;
}

int modulos(int a, int b)
{
    int mod;
    if(a % b)
    {
        mod = a % b;
        return mod;
    }

}
